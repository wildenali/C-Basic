#include <stdio.h>

int main() {

    // 1. Menampilkan Kalimat Sederhana
    printf("\nIni belajar C Programming\n");    // \n adalah enter

    // 2. Menampilkan Integer Ouput
    int iniInteger = 5;
    printf("\nNomor adalah %d\n", iniInteger);

    // 3. Menampilkan Float dan double
    float number1 = 13.5;
    double number2 = 12.4;
    printf("\nnumber1 = %f\n", number1);
    printf("number2 = %lf\n", number2);

    // 4. Menampilkan Characters
    char chr = 'a';    
    printf("\ncharacter = %c.\n", chr);

    return 0;
}